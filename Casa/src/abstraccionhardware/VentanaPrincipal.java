    private void mostrarSeleccion()
    {
        Contenedor contenedor = this.buscarElemento(String.valueOf(this.jListElementos.getSelectedValue()));
        this.jTextFieldCantidad.setText(String.valueOf(contenedor.getCantidad()));
        contenedor = null;
    }

